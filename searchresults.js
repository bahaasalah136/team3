const cardsContainer = document.getElementById("serch-item");
function displayCards(list_properties) {
  document.getElementById(
    "reslt-cont"
  ).innerHTML = `Estates :  ${list_properties.length}`;
  cardsContainer.innerHTML = "";
  if (list_properties.length != 0) {
    list_properties.forEach((property) => {
      const card = document.createElement("div");
      card.classList.add("card");
      card.innerHTML = `
      <img src="./images/${property.image}" alt="${property.name}">
                <div class="details">
                    <div>
                        <span>
                            ${property.name} for sale
                        </span>
                    </div>
                    <div>
                        <span class="h3">EGP ${property.price}</span>
                    </div>
                    <ul>
                        <li><span>3</span> bed</li>
                        <li><span>1</span> bath</li>
                        <li><span>${property.area}</span> sqft</li>
                    </ul>
                    <div class="left-right">
                        <div class="left">
                            <span>${property.location}</span>
                        </div>
                    </div>
                </div>
    `;
      console.log(property.id);
      card.addEventListener("click", function (e) {
        e.preventDefault();
        var productId = property.id;
        console.log(productId);

        window.open(`details.html?id=${productId}`, "_blank");
      });
      cardsContainer.appendChild(card);
    });
  } else {
    cardsContainer.innerHTML = `<div class="text-fond-card">There are no properties currently available</div>`;
  }
}
function searchUsers() {
  const select = document.getElementById("propertyType").value;
  const filtereproperty = select
    ? properties.filter((propertie) => propertie.name === select)
    : properties;
  displayCards(filtereproperty);
}
searchUsers();
